    NSFetchRequest *allMessagesQuery = [[NSFetchRequest alloc] initWithEntityName:@"Message"];
    self.messages = [self.dataStore.managedObjectContext executeFetchRequest:allMessagesQuery error:nil];
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return self.messages.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"BasicCellReuseID" forIndexPath:indexPath];

    Message *message = self.messages[indexPath.row];
    cell.textLabel.text = message.content;
    
    return cell;
}

